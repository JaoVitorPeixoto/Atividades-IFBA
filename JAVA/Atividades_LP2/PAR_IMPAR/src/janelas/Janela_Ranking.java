    private void CarregarTabela(){
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
		modelo.getDataVector().clear();
		
		
		try {
		for(Ranking r : dao.MostrarRanking()) {
			
			modelo.addRow(new Object[] {r.id, r.nome, r.quantidade_vitoria, r.data});
		
		}
		}catch(Exception e) {
		 throw e;
		}
    }
    
