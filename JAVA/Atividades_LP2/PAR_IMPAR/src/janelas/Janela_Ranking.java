    private void APAGARbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_APAGARbtActionPerformed
        try {
                DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

                int id = (int) modelo.getValueAt(jTable1.getSelectedRow(), 0);

                int opt = JOptionPane.showConfirmDialog(null, "Tem certeza de que quer apagar essa linha?", "Importante", 0, 3);
                if (opt == 0) {
                        dao.ApagarRanking(id);
                }

                this.CarregarTabela();

        }catch (ArrayIndexOutOfBoundsException r) {
                JOptionPane.showMessageDialog(null, "Por favor, selecione uma linha da tabela.", "Alerta", 2);
        }catch(Exception r) {
                JOptionPane.showMessageDialog(null, "ERRO!!" + r.getClass());
        }
    }//GEN-LAST:event_APAGARbtActionPerformed

